// Function to get a random image
const getRandomImage = () => {
  return imagePool[Math.floor(Math.random() * imagePool.length)];
};

const Event = ({ event }) => {
  const [showDetails, setShowDetails] = useState(false);

  // Assign a random image when the component is rendered
  const [randomImage] = useState(getRandomImage());

  const toggleDetails = () => {
    setShowDetails(!showDetails);
  };

  return (
    <li className="event">
      {/* Event image */}
      <img src={randomImage} alt="Event visual" className="event-image" />

      {/* Event title */}
      <h2 className="event-title">{event.summary}</h2>

      {/* Event location */}
      <p className="event-location">{event.location}</p>

      {/* Event date and time */}
      <p className="event-time">
        {new Date(event.start.dateTime).toLocaleString('en-GB', {
          dateStyle: 'medium',
          timeStyle: 'short',
          timeZone: event.start.timeZone,
        })}
      </p>

      {/* Details section */}
      {showDetails ? (
        <div className="event-details">
          <p className="event-description">{event.description}</p>
          <button data-testid="toggle-btn" onClick={toggleDetails}>Hide Details</button>
        </div>
      ) : (
        <button data-testid="toggle-btn" onClick={toggleDetails}>Show Details</button>
      )}
    </li>
  );
};

export default Event;
